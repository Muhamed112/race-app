import * as React from "react";
import Box from "@mui/material/Box";
import TextField from "@mui/material/TextField";
import Autocomplete from "@mui/material/Autocomplete";

export default function CountrySelect({ onCountryChange }) {
  const countriesToKeep = [
    "Bosnia and Herzegovina",
    "Croatia",
    "Serbia",
    "Macedonia",
    "Slovenia",
    "Hungary",
    "Romania",
    "Bulgaria",
    "Switzerland",
    "Austria",
    "Germany",
  ];

  // Filter out the countries that are not in the "countriesToKeep" list
  const filteredCountries = countries.filter((country) =>
    countriesToKeep.includes(country.label)
  );
  return (
    <Autocomplete
      id="country-select-demo"
      sx={{ width: "100%", marginBottom: "15px" }}
      options={filteredCountries}
      autoHighlight
      getOptionLabel={(option) => option.label}
      onChange={(event, newValue) => {
        onCountryChange(newValue?.code || ""); // Pass the selected country code to the parent component
      }}
      renderOption={(props, option) => (
        <Box
          component="li"
          sx={{ "& > img": { mr: 2, flexShrink: 0 } }}
          {...props}
        >
          <img
            loading="lazy"
            width="20"
            src={`https://flagcdn.com/w20/${option.code.toLowerCase()}.png`}
            srcSet={`https://flagcdn.com/w40/${option.code.toLowerCase()}.png 2x`}
            alt=""
          />
          {option.label} ({option.code}) {option.phone}
        </Box>
      )}
      renderInput={(params) => (
        <TextField
          {...params}
          label="Choose a country"
          inputProps={{
            ...params.inputProps,
            autoComplete: "new-password", // disable autocomplete and autofill
          }}
        />
      )}
    />
  );
}

